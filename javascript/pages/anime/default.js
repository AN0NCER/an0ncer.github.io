// Обратный индекс id -> файл правила, строится один раз при импорте модуля
const idToRule = new Map()
for (const [file, ids] of Object.entries(rules)) {
    for (const id of ids) idToRule.set(id, file)
}

export default {
    on: {
        load: async () => {
            const id = Number($ID)
            const file = idToRule.get(id)
            if (!file) return

            const rule = (await import(`./rules/${file}.js`)).default
            return rule(id)
        }
    }
}